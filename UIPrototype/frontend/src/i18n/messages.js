export function translate(lang, key, vars = {}) {
  const table = MESSAGES[lang] || MESSAGES.zh;
  let text = table[key] || MESSAGES.zh[key] || key;
  Object.entries(vars).forEach(([name, value]) => {
    text = text.replaceAll(`{${name}}`, String(value));
  });
  return text;
}
